package cn.flaty.model;


public class ClientInfo {

	private String did;

	private int appVer;

	private int os;

	public String getDid() {
		return did;
	}

	public void setDid(String did) {
		this.did = did;
	}

	public int getAppVer() {
		return appVer;
	}

	public void setAppVer(int appVer) {
		this.appVer = appVer;
	}

	public int getOs() {
		return os;
	}

	public void setOs(int os) {
		this.os = os;
	}



}
